# | Function       | Purpose                      |
# | -------------- | ---------------------------- |
# | `re.match()`   | Matches pattern at **start** |
# | `re.search()`  | Searches anywhere in string  |
# | `re.findall()` | Returns all matches          |
# | `re.split()`   | Splits string                |
# | `re.sub()`     | Replaces text                |
#
# | Symbol  | Meaning         |
# | ------- | --------------- |
# | `.`     | Any character   |
# | `^`     | Start of string |
# | `$`     | End of string   |
# | `*`     | 0 or more       |
# | `+`     | 1 or more       |
# | `?`     | 0 or 1          |
# | `{n}`   | Exactly n       |
# | `{n,m}` | Between n & m   |
# | `\d`    | Digit           |
# | `\D`    | Non-digit       |
# | `\w`    | Word char       |
# | `\W`    | Non-word        |
# | `\s`    | Whitespace      |
# | `\S`    | Non-whitespace  |

# | Pattern  | Matches     |
# | -------- | ----------- |
# | `[abc]`  | a or b or c |
# | `[0-9]`  | digits      |
# | `[a-z]`  | lowercase   |
# | `[^abc]` | NOT a,b,c   |
